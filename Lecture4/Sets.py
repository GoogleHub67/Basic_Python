#Sets have a random order
set1 = {1, 2, 3, 4}
print(set1)
print(type(set1))
print(len(set1))

#Duplicate Values are ignored
set2 = {1, 2, 2, 2, "hello", "world", "world"}
print(set2)
print(type(set2))
print(len(set2))

set3 = {1, 2, 2, 2, "hello", "world", "world", 4}
print(set3)
print(type(set3))
print(len(set3))

set4 = {} #Empty DICTIONARY
print(type(set4))

set5 = set()
print(type(set5))